#include "../server.hpp"

namespace Carbonide { namespace Server
{
//#####################################################################################################################
    void CarbonideServer::setupWebInterface()
    {
        app_.get("/", [this](auto req, auto res)
        {
            res->redirect("/web").end();
        });

        app_.get("/web", [this](auto req, auto res)
        {
            res->send_status(200);
        });
    }
//---------------------------------------------------------------------------------------------------------------------
//#####################################################################################################################
}
}
